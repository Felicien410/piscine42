#include<unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	y;

	y = 0;
	while (str[y])
	{
		if (!(str[y] >= 'a' && str[y] <= 'z')
			&& !(str[y] >= 'A' && str[y] <= 'Z'))
		{
			return (0);
		}
		y++;
	}
	return (1);
}
